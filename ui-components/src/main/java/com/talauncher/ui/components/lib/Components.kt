package com.talauncher.ui.components.lib

/**
 * TALauncher UI Components Library
 *
 * A collection of reusable, generic UI components following Material Design 3 principles
 * with custom adaptations for the TALauncher project.
 *
 * Available components:
 *
 * Foundation:
 * - ComponentDefaults: Design system constants and defaults
 * - ComponentDensity: Size variants for components
 * - ComponentVariant: Style variants for components
 *
 * Cards:
 * - GenericCard: Flexible card component with glassmorphism support
 * - SimpleCard: Basic card with semantic variants
 *
 * Buttons:
 * - GenericButton: Button with multiple variants and densities
 * - ActionButton: Semantically colored action buttons
 *
 * Chips:
 * - GenericChip: Selectable chip component
 * - SimpleFilterChip: Material3 filter chip wrapper
 * - ActionChip: Non-selectable action chips
 *
 * Lists:
 * - GenericListItem: Flexible list item component
 * - IconListItem: List item with icon avatar
 * - TextAvatarListItem: List item with text avatar
 * - ListItemAction: Action button for list items
 *
 * Inputs:
 * - GenericTextField: Flexible text input with glassmorphism
 * - SearchField: Specialized search input
 * - NumericField: Numeric input with validation
 *
 * Usage:
 * Import specific components as needed:
 * ```
 * import com.talauncher.ui.components.lib.cards.GenericCard
 * import com.talauncher.ui.components.lib.buttons.GenericButton
 * ```
 */